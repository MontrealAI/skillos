"""Apply the SkillOS public-safe copy policy.

This script is intentionally idempotent: it can run on every GitHub Actions build.
It updates public-facing copy to say "reference workflow" and "demo assumptions"
instead of implying audited customer results or guaranteed financial outcomes.
"""
